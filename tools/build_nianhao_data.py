def themes_of(name: str) -> list[str]:
    themes: list[str] = []
    for ch in name:
        if ch in THEME_CHARS and ch not in themes:
            themes.append(ch)
    if name and name[0] not in themes:
        themes.insert(0, name[0])
    return themes[:4] if themes else [name[0]]


def tone_of(name: str) -> str:
    if name in MITIGATE:
        return "mitigate"
    if name in AUSPICIOUS:
        return "auspicious"
    for ch in name:
        if ch in "康安宁平泰和顺治定绥靖保清":
            return "mitigate"
        if ch in "天开元盛大德福寿祥瑞龙凤":
            return "auspicious"
    return "neutral"


def main() -> None:
    seen: set[str] = set()
    names: list[str] = []
    for token in RAW.split():
        name = token.strip()
        if not name or len(name) < 2 or name in SKIP or name in seen:
            continue
        seen.add(name)
        names.append(name)

    entries = []
    for name in names:
        themes = themes_of(name)
        tone = tone_of(name)
        weight = 10 if len(name) == 2 else (4 if len(name) == 3 else 2)
        if tone == "auspicious":
            weight += 2
        elif tone == "mitigate":
            weight += 1
        entries.append(
            {
                "name": name,
                "themes": themes,
                "tone": tone,
                "weight": weight,
            }
        )

    out = Path(__file__).resolve().parents[1] / "dynasty" / "nianhao_data.py"
    lines = [
        "# -*- coding: utf-8 -*-",
        '"""历史年号候选库：带主题字与吉凶向，供语义主题链选号。"""',
        "",
        "NIANHAO_ENTRIES = [",
    ]
    for entry in entries:
        lines.append(f"    {entry!r},")
    lines.extend(
        [
            "]",
            "",
            'NIANHAO_NAMES = [e["name"] for e in NIANHAO_ENTRIES]',
            'NIANHAO_BY_NAME = {e["name"]: e for e in NIANHAO_ENTRIES}',
            "",
        ]
    )
    out.write_text("\n".join(lines), encoding="utf-8")
    print(f"wrote {out} ({len(entries)} entries)")
    tones = {t: sum(1 for e in entries if e["tone"] == t) for t in ("auspicious", "neutral", "mitigate")}
    print("tones", tones)


if __name__ == "__main__":
    main()
